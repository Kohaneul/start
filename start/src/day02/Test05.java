package day02;

public class Test05 {
	public static void main(String[] args) {
		//3.14를 float 타입 변수 pi에 기억해보자.
		
		float pi; //변수 선언
		
		pi = 3.14f; //리터럴 형변환(기본적으로 데이터를 저장 하지 않으면 실수는 double 타입으로 저장하기 때문(double 8byte> float 4byte))
		
	}
	
	/*
	 	참고] 
	 		개발자가 입력하는 정수형 데이터는 
	 		특별한 조작없이는 모든 데이터형이 기억될 수 있다.
	 		(실수형만 신경쓰면 된다. float 리터럴 형변환=>숫자 뒤에 f)
	 		
	 		ex) 
	 			int a = 100;
	 			byte b = 100;
	 			short c = 100;
	 			long d = 100;
	 		
	 		
	 */
	

}
